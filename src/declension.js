// Підрозділи в родовому відмінку
// Значення pidrozdil приходять як "3-го стрілецького відділення 2-го стрілецького взводу 3-ї стрілецької роти"
// Вони вже в родовому — просто повертаємо як є
export function getPidrozdilRodovyi(pidrozdil) {
  return pidrozdil || ''
}

function declineWord(word) {
  const w = word.trim()
  if (!w) return w
  const isUpper = w === w.toUpperCase() && w.length > 1
  if (isUpper) {
    if (w.endsWith('ЕНКО')) return w.slice(0, -2) + 'КА'
    if (w.endsWith('КО') || w.endsWith('ЧО')) return w
    if (w.endsWith('О')) return w.slice(0, -1) + 'А'
    if (w.endsWith('А')) return w.slice(0, -1) + 'И'
    if (w.endsWith('ИЙ') || w.endsWith('ІЙ')) return w.slice(0, -2) + 'ОГО'
    return w + 'А'
  } else {
    if (w.endsWith('ій')) return w.slice(0, -2) + 'ого'
    if (w.endsWith('й')) return w.slice(0, -1) + 'я'
    if (w.endsWith('ло') || w.endsWith('но')) return w
    if (w.endsWith('о')) return w.slice(0, -1) + 'а'
    if (w.endsWith('а')) return w.slice(0, -1) + 'и'
    return w + 'а'
  }
}

export function getPibRodovyi(pib) {
  if (!pib) return ''
  return pib.trim().split(' ').map(declineWord).join(' ')
}

export function getPibShort(pib) {
  if (!pib) return ''
  const parts = pib.trim().split(' ')
  const [prizvysche, imya] = parts
  if (!imya) return pib
  return `${imya} ${prizvysche}`
}

